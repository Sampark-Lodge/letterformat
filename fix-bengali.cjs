function fixPage(filePath, pageData) {
  let content = fs.readFileSync(filePath, 'utf-8');
  
  // Fix title
  content = content.replace(/<title>.*?<\/title>/, `<title>${pageData.title} — LetterFormat.in</title>`);
  
  // Fix format title in body
  content = content.replace(/<h1 class="format-title">.*?<\/h1>/, `<h1 class="format-title">${pageData.title}</h1>`);
  
  // Fix recipient
  content = content.replace(/<div class="to-name">.*?<\/div>/, `<div class="to-name"><span class="placeholder" contenteditable="true">${pageData.recipient}</span></div>`);
  
  // Fix subject
  content = content.replace(/<div class="sub-text">.*?<\/div>/, `<div class="sub-text"><span class="placeholder" contenteditable="true">${pageData.subject}</span></div>`);
  
  // Fix body paragraphs
  const bodyHtml = pageData.body.map(p => `<p>${p}</p>`).join('\n            ');
  content = content.replace(/<div class="letter-body">[\s\S]*?<\/div>/, `<div class="letter-body">\n            ${bodyHtml}\n          </div>`);
  
  // Fix description
  content = content.replace(/<p class="format-desc">.*?<\/p>/, `<p class="format-desc">বিনামূল্যে ${pageData.title}। কপি-পেস্ট ফরম্যাট, এডিটেবল টেমপ্লেট, পূরণ করা নমুনা, টিপস সহ। আপডেট ২০২৬।</p>`);
  
  fs.writeFileSync(filePath, content);
  console.log(`✓ ${path.basename(filePath)}`);
}

