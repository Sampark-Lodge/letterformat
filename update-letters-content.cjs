// Now generate more detailed content using template
const formatsDir = './formats';

// For each new file, update with proper Bengali content
Object.keys(bengaliContent).forEach(file => {
  const filePath = path.join(formatsDir, file);
  if (fs.existsSync(filePath)) {
    let content = fs.readFileSync(filePath, 'utf-8');
    
    // Update letter content
    const data = bengaliContent[file];
    
    // Find and replace letter body
    content = content.replace(
      /<div class="letter-body">[\s\S]*?<\/div>/,
      `<div class="letter-body">${data.body}</div>`
    );
    
    // Update tips
    content = content.replace(
      /<div class="tip-item">[\s\S]*?<\/div>\s*<\/div>\s*<\/div>/,
      data.tips.map((tip, i) => `<div class="tip-item"><div class="tip-num">${i+1}</div><div class="tip-text">${tip}</div></div>`).join('') + '</div></div>'
    );
    
    // Update FAQ
    content = content.replace(
      /<div class="faq-item">[\s\S]*?<\/div>\s*<\/div>\s*<\/div>/,
      data.faq.map((item, i) => {
        const [q, a] = item.split(' - ');
        return `<div class="faq-item"><h3>❓ ${q}</h3><p>${a}</p></div>`;
      }).join('') + '</div></div>'
    );
    
    fs.writeFileSync(filePath, content);
    console.log(`✅ Updated: ${file}`);
  }
});

console.log('\n✅ Updated application letters with Bengali content');