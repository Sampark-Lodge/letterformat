function buildFAQHTML(faqs) {
  return faqs.map(faq => {
    const escapedQ = faq.q.replace(/"/g, '&quot;');
    const escapedA = faq.a.replace(/"/g, '&quot;');
    return `<div class="faq-item"><h3>❓ ${faq.q}</h3><p>${faq.a}</p></div>`;
  }).join('\n          ');
}

function addFAQs(filepath, faqs) {
  let content = fs.readFileSync(filepath, 'utf8');
  
  // Find the FAQ section - match from faq-box opening to sidebar opening
  // Structure varies: some files have 1 main-content closing div, others have more nested divs
  const faqBoxRegex = /<div class="faq-box">[\s\S]*?<\/div>[\s\S]*?<div class="sidebar">/;
  const faqBoxMatch = content.match(faqBoxRegex);
  if (!faqBoxMatch) {
    return `❌ No faq-box found: ${path.basename(filepath)}`;
  }
  
  const newFAQs = buildFAQHTML(faqs);
  const faqSection = `<div class="faq-box">
        <h3 style="font-size:0.95rem;font-weight:700;color:var(--gray-900);margin-bottom:14px;">❓ সচরাচর জিজ্ঞাসা</h3>
          ${newFAQs}
      </div>`;
  
  // Extract closing structure: everything after the last </div> (faq-box closing) up to sidebar
  const match = faqBoxMatch[0];
  const lastDivIdx = match.lastIndexOf('</div>');
  const afterFaqBox = match.slice(lastDivIdx + 6); // +6 for '</div>'
  const sidebarIdx = afterFaqBox.indexOf('<div class="sidebar">');
  const closingStructure = afterFaqBox.slice(0, sidebarIdx); // preserve main-content closing, comments, etc.
  
  const newContent = content.replace(faqBoxRegex, faqSection + closingStructure + '<div class="sidebar">');
  
  fs.writeFileSync(filepath, newContent, 'utf8');
  return `✅ ${path.basename(filepath)} (${faqs.length} FAQs)`;
}

// Also update the JSON-LD FAQ schema
function updateFAQSchema(filepath, faqs) {
  let content = fs.readFileSync(filepath, 'utf8');
  
  // Build FAQPage schema
  const mainEntity = faqs.map(faq => {
    return `      {
        "@type": "Question",
        "name": "${faq.q.replace(/"/g, '\\"')}",
        "acceptedAnswer": {
          "@type": "Answer",
          "text": "${faq.a.replace(/"/g, '\\"')}"
        }
      }`;
  }).join(',\n');
  
  const faqSchema = `  <script type="application/ld+json">
  {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": [
${mainEntity}
    ]
  }
  </script>`;
  
  // Replace existing FAQPage schema
  const faqSchemaRegex = /<script type="application\/ld\+json">\s*\{[\s\S]*?"@type":\s*"FAQPage"[\s\S]*?<\/script>/;
  const newContent = content.replace(faqSchemaRegex, faqSchema);
  
  fs.writeFileSync(filepath, newContent, 'utf8');
}

